public class switchCaseStatement {
    public static void main(String[]args){
        int i=2;
        switch(i)
        {
            case 1:
                System.out.println("TalkMe1");
                break;

            case 2:
                System.out.println("TalkMe2");
                break;

            case 3:
                System.out.println("TalkMe3");
                break;
            case 4:
                System.out.println("TalkMe4");
                break;

                default:
                    System.out.println("Default");

        }
    }
}
